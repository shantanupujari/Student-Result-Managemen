    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        String Rollnum = jTextField11.getText();
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srm", "root","shantanu@123");
            Statement st=con.createStatement();               
            ResultSet rs = st.executeQuery("select * from student inner join result where student.Rollnum='"+Rollnum+"'and result.RollNum='"+Rollnum+"' ");
            if (rs.next()){
                jTextField3.setText(rs.getString(4));
                jTextField4.setText(rs.getString(3));
                jTextField5.setText(rs.getString(2));
                jTextField2.setText(rs.getString(5));
                jTextField6.setText(rs.getString(7));
                jTextField7.setText(rs.getString(8));
                jTextField8.setText(rs.getString(9));
                jTextField9.setText(rs.getString(10));
                
                int j1=Integer.parseInt(jTextField6.getText());
                int j2=Integer.parseInt(jTextField7.getText());
                int j3=Integer.parseInt(jTextField8.getText());
                int j4=Integer.parseInt(jTextField9.getText());
                
                int result= j1+j2+j3+j4;
                jTextField10.setText(String.valueOf(result));
                if (result>=120 && j1>=30&j2>=30&j3>=30&j4>=30){
                    jTextField1.setBackground(new java.awt.Color(0,255,102));
                    jTextField1.setText("PASS");
                }
                else {
                    jTextField1.setBackground(new java.awt.Color(204, 0, 51));
                    jTextField1.setText("FAIL");
                }
            }
            else {
                 JOptionPane.showMessageDialog(null, "Incorrect roll number");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_formComponentShown

